int main() {
	long score;
	cin >> score;
	if (score >= 91) {
		cout << "A*";
	} else if (score>=75 && score<=90) {
		cout << "A";
	} else if (score>=60 && score<=74) {
		cout << "B";
	} else if (score>=50 && score<=59) {
		cout << "C";
	} else if (score>=35 && score<=49) {
		cout << "D";
	} else if (score>=20 && score<=34) {
		cout << "E";
	} else {
		cout << "U";
	}
	return 0;
}
/* A*: 91 and above
A: 75 to 90
B: 60 to 74
C: 50 to 59
D: 35 to 49
E: 20 to 34
U: Below 20 */

